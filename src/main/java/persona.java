public class persona {
    private int cedula;
    private String nombre;
    private int edad;
    public int getCedula()
    {
        return cedula;
    }
    public void setCedula(int cedula){
        this.cedula= cedula;
    }
    public String getNombre(){
        return nombre;
    }
    public void setNombre(String nombre){
        this.nombre= nombre;
    }
    public int getEdad(){
        return edad;
    }
    public void setEdad(int edad){
        this.edad=edad;
    }
}
